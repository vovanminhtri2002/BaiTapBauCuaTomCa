    public void laydulieuBauCua(ArrayList<Integer> dsrandom){
        int j = tblBang.getRowCount();
        String Cuoc = cboChon.getSelectedItem().toString();
        int TienCuoc = Integer.parseInt(txtTienCuoc.getText());
        if(TienCuoc>Integer.parseInt(txtTienConLai.getText()))
            JOptionPane.showMessageDialog(this, "Số tiền cược đã lớn hơn số tiền còn");
        else{
            String tinhtrang = setTinhTrang(dsrandom);
            int SoTienCon = 0;
            if(tinhtrang=="Thua")
                SoTienCon = Integer.parseInt(txtTienConLai.getText())-TienCuoc;
            else
                SoTienCon = Integer.parseInt(txtTienConLai.getText())+TienCuoc;
            txtTienConLai.setText(SoTienCon+"");
            Vector v = new Vector();
            v.add(j+1); 
            v.add(Cuoc); 
            v.add(TienCuoc); 
            v.add(tinhtrang);
            v.add(SoTienCon);
            j++;
            tblData.add(v);
            tblBang.setModel(new DefaultTableModel(tblData, tblTitle));
        }
    }
    
    public String setTinhTrang(ArrayList<Integer> dsrandom){
        int cuoc = cboChon.getSelectedIndex()+1;
        String tinhtrang = "Thua";
        for(int i:dsrandom){
            if(i==cuoc){
                tinhtrang="Thắng";
                System.out.println("Số thắng: "+i);
                break;
            }
        }
        return tinhtrang;
    }
    
