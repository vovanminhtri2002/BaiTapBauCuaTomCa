    public void RandomKQ(ArrayList<Integer> dsrandom){
        Random x = new Random();
        for(int i=1;i<=3;i++)
        {
            int n = x.nextInt(6)+1;
            System.out.println(n);
            dsrandom.add(n);
            String linkimg = LayLinkHinh(n);
            ImageIcon imgicon = new ImageIcon(new ImageIcon(linkimg).getImage().getScaledInstance(100, 100, Image.SCALE_SMOOTH));
            if(i==1)
                lblKetQua1.setIcon(imgicon);
            if(i==2)
                lblKetQua2.setIcon(imgicon);
            if(i==3)
                lblKetQua3.setIcon(imgicon);
        }
    }
